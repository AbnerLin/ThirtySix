package com.thirtySix.model;

import java.util.ArrayList;
import java.util.List;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.OneToMany;
import javax.persistence.Table;

import org.hibernate.annotations.GenericGenerator;
import org.hibernate.annotations.NotFound;
import org.hibernate.annotations.NotFoundAction;

@Entity
@Table(name = "SEATMAP")
public class SeatMap {

	/**
	 * 地圖編號
	 */
	@Id
	@GeneratedValue(generator = "UUID")
	@GenericGenerator(name = "UUID", strategy = "com.thirtySix.model.id.generator.UuidGenerator")
	@Column(name = "MAPID")
	private String mapID;

	/**
	 * 位置
	 */
	@Column(name = "NAME")
	private String name;

	/**
	 * 地圖寬度 px
	 */
	@Column(name = "WIDTH")
	private int width;

	/**
	 * 地圖高度 px
	 */
	@Column(name = "HEIGHT")
	private int height;

	/**
	 * 擺設座標位置
	 */
	@NotFound(action = NotFoundAction.IGNORE)
	@OneToMany(fetch = FetchType.EAGER, mappedBy = "seatMap")
	private List<Furnish> furnishList = new ArrayList<Furnish>();

	/**
	 * 取得地圖編號
	 * 
	 * @return
	 */
	public String getMapID() {
		return this.mapID;
	}

	/**
	 * 設定地圖編號
	 * 
	 * @param mapID
	 */
	public void setMapID(final String mapID) {
		this.mapID = mapID;
	}

	/**
	 * Get name of map.
	 * 
	 * @return
	 */
	public String getName() {
		return this.name;
	}

	/**
	 * Set name of map.
	 * 
	 * @param location
	 */
	public void setName(final String name) {
		this.name = name;
	}

	/**
	 * 取得地圖寬度
	 * 
	 * @return
	 */
	public int getWidth() {
		return this.width;
	}

	/**
	 * 設定地圖寬度
	 * 
	 * @param width
	 */
	public void setWidth(final int width) {
		this.width = width;
	}

	/**
	 * 取得地圖高度
	 * 
	 * @return
	 */
	public int getHeight() {
		return this.height;
	}

	/**
	 * 設定地圖高度
	 * 
	 * @param height
	 */
	public void setHeight(final int height) {
		this.height = height;
	}

	/**
	 * 取得擺設座標清單
	 * 
	 * @return
	 */
	public List<Furnish> getFurnishList() {
		return this.furnishList;
	}

	/**
	 * 設定擺設座標清單
	 * 
	 * @param seatPositionList
	 */
	public void setFurnishList(final List<Furnish> setFurnishList) {
		this.furnishList = setFurnishList;
	}

}
